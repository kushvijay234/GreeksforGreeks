/* 
* Nested For Loop
*
*/

for (let i = 1; i <= 10; i++) {
    for (let j = 1; j <= 10; j++) {
        // console.log(`The Value of i is ${i} and j is ${j}`);

        // table 
        let product = i * j ;
        console.log(`${i} X ${j} = ${product}`) 
    }
    console.log("---------------------------");
}

